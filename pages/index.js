import { useEffect, useState } from 'react';
import Head from 'next/head';
import Image from 'next/image';

import Banner from '../components/banner';
import Card from '../components/card';

// import coffeeStoresData from '../data/coffee-store.json';
import { fetchCoffeeStores } from '../lib/coffee-stores';
import useTrackLocation from '../hooks/use-track-location';
import styles from '../styles/Home.module.css';

// Here we used getStaticProps for pre-fetching then coffee-shop content
// Anything in the getStaticProps is Server Side code
export async function getStaticProps(context) {
  const coffeeStores = await fetchCoffeeStores();
  return {
    props: {
      coffeeStores,
    }, // will be passed to the page component as props
  };
}

// Any thing in the Home Component is Client Side Code
export default function Home(props) {
  console.log('props', props);
  const { handleTrackLocation, latLong, locationErrorMsg, isFindingLocation } =
    useTrackLocation();
  console.log({ latLong, locationErrorMsg });

  const [coffeeStores, setCoffeeStores] = useState('');
  const [coffeeStoresError, setCoffeeStoresError] = useState(null);

  useEffect(() => {
    // {
    //   window.navigator;
    // }
    (async function () {
      if (latLong) {
        try {
          const fetchedCoffeeStores = await fetchCoffeeStores(latLong, 30);
          console.log({ fetchedCoffeeStores });
          setCoffeeStores(fetchedCoffeeStores);
        } catch (error) {
          console.log({ error });
          setCoffeeStoresError(error.message);
        }
      }
    })();
  }, [latLong]);

  const handleOnBannerBtnClick = (e) => {
    e.preventDefault();
    console.log('Banner btn clicked');
    handleTrackLocation();
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>â˜•Coffee Corner</title>
        <meta name="description" content="Generated by create next app" />
      </Head>

      <main className={styles.main}>
        <Banner
          buttonText={isFindingLocation ? 'Loading...' : 'View stores nearby'}
          handleOnClick={handleOnBannerBtnClick}
        />
        {locationErrorMsg && <p>Something went wrong: {locationErrorMsg} </p>}
        {coffeeStoresError && <p>Something went wrong: {coffeeStoresError} </p>}
        <div className={styles.heroImage}>
          <Image
            src="/static/hero-image.webp"
            alt=""
            width={700}
            height={400}
          />
        </div>
        {coffeeStores.length > 0 && (
          <>
            <h2 className={styles.heading2}>Coffee Stores near me</h2>
            <div className={styles.cardLayout}>
              {coffeeStores.map((coffeeStores) => {
                return (
                  <Card
                    key={coffeeStores.id}
                    name={coffeeStores.name}
                    imageUrl={coffeeStores.imgUrl}
                    href={`/coffee-store/${coffeeStores.id}`}
                    className={styles.card}
                  />
                );
              })}
            </div>
          </>
        )}

        {props.coffeeStores.length > 0 && (
          <div className={styles.sectionWrapper}>
            <h2 className={styles.heading2}>Toronto Coffee Store</h2>
            <div className={styles.cardLayout}>
              {props.coffeeStores.map((coffeeStore) => {
                // console.log(coffeeStore.fsq_id)
                return (
                  <Card
                    key={coffeeStore.id}
                    name={coffeeStore.name}
                    imageUrl={
                      coffeeStore.imgUrl ||
                      'https://images.unsplash.com/photo-1504753793650-d4a2b783c15e?ixid=MnwxMjA3fDB8MHxwaG90by1wYWdlfHx8fGVufDB8fHx8&ixlib=rb-1.2.1&auto=format&fit=crop&w=2000&q=80'
                    }
                    href={`/coffee-store/${coffeeStore.id}`}
                    className={styles.card}
                  />
                );
              })}
            </div>
          </div>
        )}
      </main>
    </div>
  );
}
